// Split the single-source book (docs/Books/LplKernel_Book.md) into one Markdown
// file per chapter/annexe under src/content/book/ (generated, gitignored).
// The source stays the single source of truth; this runs on predev/prebuild.
import { readFileSync, writeFileSync, mkdirSync, rmSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const SRC = join(here, "..", "..", "docs", "Books", "LplKernel_Book.md");
const OUT = join(here, "..", "src", "content", "book");
const META = join(here, "..", "src", "generated", "book-meta.ts");

const raw = readFileSync(SRC, "utf8");
const lines = raw.split("\n");

// A top-level heading is exactly "# " (parts, chapters, annexes, title).
const isTop = (l) => /^# (?!#)/.test(l);

let bookTitle = "LplKernel";
let bookSubtitle = "";
let currentPart = null;
const entries = [];
let cur = null;
let sawTitle = false;

const flush = () => {
  if (cur) {
    cur.body = cur.bodyLines.join("\n").trim();
    delete cur.bodyLines;
    entries.push(cur);
    cur = null;
  }
};

const classify = (text) => {
  if (/^Préambule/i.test(text)) return { kind: "preamble", slug: "preambule" };
  const ch = text.match(/^Chapitre\s+(\d+)/i);
  if (ch) return { kind: "chapter", slug: `chapitre-${ch[1]}`, num: Number(ch[1]) };
  const an = text.match(/^Annexe\s+([A-Za-z])/i);
  if (an) return { kind: "annexe", slug: `annexe-${an[1].toLowerCase()}` };
  if (/^(Épilogue|Conclusion|Postface)/i.test(text)) {
    return { kind: "epilogue", slug: text.toLowerCase().replace(/[^a-z]+/g, "-").replace(/^-|-$/g, "") };
  }
  return null;
};

for (let i = 0; i < lines.length; i++) {
  const line = lines[i];
  if (isTop(line)) {
    const text = line.replace(/^#\s+/, "").trim();

    // First top-level heading = the book title.
    if (!sawTitle && /lplkernel/i.test(text)) {
      sawTitle = true;
      bookTitle = text;
      // Subtitle: the next "## ..." line.
      for (let j = i + 1; j < Math.min(i + 4, lines.length); j++) {
        if (/^## /.test(lines[j])) {
          bookSubtitle = lines[j].replace(/^##\s+/, "").replace(/[*_]/g, "").trim();
          break;
        }
      }
      continue;
    }

    if (/^PARTIE/i.test(text)) {
      flush();
      currentPart = text;
      continue;
    }

    const meta = classify(text);
    if (meta) {
      flush();
      cur = {
        title: text,
        part: meta.kind === "chapter" ? currentPart : null,
        kind: meta.kind,
        slug: meta.slug,
        num: meta.num ?? null,
        order: entries.length,
        bodyLines: [],
      };
      continue;
    }
    // Any other top-level heading: keep it inside the current entry body.
  }
  if (cur) cur.bodyLines.push(line);
}
flush();

// (Re)create the output directory.
rmSync(OUT, { recursive: true, force: true });
mkdirSync(OUT, { recursive: true });
if (!existsSync(dirname(META))) mkdirSync(dirname(META), { recursive: true });

entries.forEach((e, idx) => {
  const order = idx;
  const fm = [
    "---",
    `title: ${JSON.stringify(e.title)}`,
    `slug: ${JSON.stringify(e.slug)}`,
    `order: ${order}`,
    `kind: ${JSON.stringify(e.kind)}`,
    `part: ${e.part ? JSON.stringify(e.part) : "null"}`,
    e.num != null ? `num: ${e.num}` : `num: null`,
    "---",
    "",
  ].join("\n");
  const name = `${String(order).padStart(2, "0")}-${e.slug}.md`;
  writeFileSync(join(OUT, name), fm + e.body + "\n", "utf8");
});

const metaObj = { title: bookTitle, subtitle: bookSubtitle, count: entries.length };
writeFileSync(
  META,
  `// Generated by scripts/split-book.mjs — do not edit.\nexport default ${JSON.stringify(metaObj, null, 2)} as const;\n`,
);

console.log(`[split-book] ${entries.length} sections -> ${OUT}`);
